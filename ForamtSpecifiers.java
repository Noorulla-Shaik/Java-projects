// foramt specifiers same as c language
class ForamtSpecifiers {
    public static void main(String[] args) {
        int number = 10;
        String name = "YOUR NAME";
        float value = 10.5f;
        System.out.println("%d\n="+number);
        System.out.println("%f\n="+value);
        System.out.println("%s\n="+name);
    }
}
